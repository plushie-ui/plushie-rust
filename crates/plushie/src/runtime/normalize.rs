//! Tree normalization: scope prefixing, ID validation, a11y rewrites.
//!
//! After `App::view()` returns a `View` (TreeNode), normalization
//! walks the tree to apply scoped ID prefixes and validate ID
//! constraints. A second pass rewrites cross-widget accessibility
//! references (`labelled_by`, `described_by`, `error_message`,
//! `active_descendant`, `radio_group`) through the same scope-
//! prefix logic, populates implicit radio groups from the shared
//! `group` prop on radios, and fills in the accessible role from
//! the widget type when the author did not set one.
//!
//! ## Scoping rules
//!
//! Containers with explicit (non-auto) IDs create a scope. Their
//! children's IDs are prefixed with the scope chain joined by `/`.
//! Two node types are exempt:
//!
//! - **Auto IDs** (prefixed `auto:`): generated by the DSL, not
//!   user-chosen. They don't participate in scoping and bypass
//!   duplicate detection (the same call site in a loop produces
//!   identical auto IDs).
//! - **Window nodes** (`type_name == "window"`): top-level layout
//!   containers that don't scope their children.
//!
//! ## ID constraints
//!
//! - `/` is reserved as the scope separator and rejected in
//!   user-provided IDs.
//! - Duplicate explicit IDs within a tree produce a warning.
//!
//! ## Depth cap
//!
//! Recursion is capped at
//! [`plushie_widget_sdk::shared_state::MAX_TREE_DEPTH`]. Beyond the
//! cap the subtree is truncated (the returned node has no children)
//! and a `tree_too_deep` diagnostic is appended. Render and
//! prepare_walk apply the same cap so defence-in-depth holds even
//! if the host sends a pathologically nested tree.
//!
//! ## A11y rewrites
//!
//! Cross-widget references inside the `a11y` prop are scope-rewritten
//! the same way widget IDs are: a reference to `heading` inside a
//! scoped `form` container becomes `main#form/heading`. References
//! that cannot be resolved to any declared ID in the normalized
//! tree produce an `a11y_ref_unresolved` diagnostic.
//!
//! Radio widgets sharing the same `group` prop value are collected
//! into an implicit radio group: each member's `a11y.radio_group` is
//! populated with the scoped IDs of its peers. This mirrors the
//! HTML `<input type="radio" name="x">` model where grouping is by
//! name, not DOM position.
//!
//! `a11y.role` is auto-populated from the widget type when the
//! author has not set one. `automation::Element::inferred_role`
//! reads this normalized prop directly.

use std::collections::{BTreeMap, HashSet};

use plushie_core::protocol::{PropValue, TreeNode};
use plushie_widget_sdk::shared_state::MAX_TREE_DEPTH;

/// Normalize a view tree: apply scope prefixes, validate IDs,
/// rewrite cross-widget a11y references, and auto-populate a11y
/// defaults (role, radio_group).
///
/// Returns the normalized tree and any validation warnings
/// (duplicate IDs, reserved characters, unresolved a11y refs).
pub fn normalize(tree: &TreeNode) -> (TreeNode, Vec<String>) {
    let mut warnings = Vec::new();
    let mut seen_ids = HashSet::new();
    let result = normalize_node(tree, "", &mut seen_ids, &mut warnings, 0);

    // Second pass: rewrite a11y refs, populate radio groups and role
    // defaults. Uses the scoped IDs gathered during the first pass so
    // we can both rewrite and validate references.
    let declared_ids = collect_ids(&result);
    let radio_groups = collect_radio_groups(&result);
    let rewritten = rewrite_a11y(&result, "", &declared_ids, &radio_groups, &mut warnings, 0);

    // Third pass: walk the finished tree looking for interactive
    // widgets that have no accessible name at all. Emits
    // `missing_accessible_name` diagnostics with the scoped ID of
    // the offender. Catches icon-only toolbars that would otherwise
    // render a focusable node with no screen-reader label.
    check_missing_accessible_name(&rewritten, &mut warnings);

    (rewritten, warnings)
}

fn normalize_node(
    node: &TreeNode,
    scope: &str,
    seen_ids: &mut HashSet<String>,
    warnings: &mut Vec<String>,
    depth: usize,
) -> TreeNode {
    if depth > MAX_TREE_DEPTH {
        // Truncate the subtree to stop runaway recursion. The current
        // node is returned with no children; a diagnostic records the
        // truncation so hosts can surface it.
        log::error!(
            "[code=tree_too_deep][id={}] normalize depth exceeds {}, truncating subtree",
            node.id,
            MAX_TREE_DEPTH
        );
        warnings.push(format!(
            "tree_too_deep: subtree rooted at \"{}\" exceeds MAX_TREE_DEPTH={}, truncating",
            node.id, MAX_TREE_DEPTH
        ));
        return TreeNode {
            id: node.id.clone(),
            type_name: node.type_name.clone(),
            props: node.props.clone(),
            children: vec![],
        };
    }
    let id = &node.id;
    let type_name = &node.type_name;

    let is_auto = id.starts_with("auto:");
    let is_window = type_name == "window";

    // Build the scoped ID.
    // Window nodes keep their bare ID. Children of windows get "window#id".
    // Deeper descendants get "window#parent/id" (# only at window boundary).
    let scoped_id = if is_auto || scope.is_empty() {
        id.clone()
    } else if scope.ends_with('#') {
        format!("{scope}{id}")
    } else {
        format!("{scope}/{id}")
    };

    // Check for duplicate IDs (only for non-auto IDs).
    if !is_auto && !scoped_id.is_empty() && !seen_ids.insert(scoped_id.clone()) {
        warnings.push(format!("duplicate ID: \"{scoped_id}\""));
    }

    // Check for reserved characters in user-provided IDs.
    if !is_auto {
        if id.contains('/') {
            warnings.push(format!(
                "ID \"{id}\" contains reserved character '/'. \
                 Use container scoping instead."
            ));
        }
        if id.contains('#') {
            warnings.push(format!(
                "ID \"{id}\" contains reserved character '#'. \
                 '#' is reserved for window-qualified paths."
            ));
        }
    }

    // Build the scope for children.
    // Window nodes set "window#" as the child scope.
    // Named non-window nodes propagate their scoped ID.
    // Auto-ID nodes are transparent.
    let child_scope = if is_window {
        format!("{scoped_id}#")
    } else if is_auto || id.is_empty() {
        scope.to_string()
    } else {
        scoped_id.clone()
    };

    // Normalize children recursively.
    let children = node
        .children
        .iter()
        .map(|child| normalize_node(child, &child_scope, seen_ids, warnings, depth + 1))
        .collect();

    TreeNode {
        id: scoped_id,
        type_name: node.type_name.clone(),
        props: node.props.clone(),
        children,
    }
}

